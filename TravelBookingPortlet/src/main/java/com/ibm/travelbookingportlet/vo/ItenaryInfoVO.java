package com.ibm.travelbookingportlet.vo;

import java.io.Serializable;

public class ItenaryInfoVO implements Serializable {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String date;
	private String day;
	private String hotelImage;
	private String hotelInfo;
	private String hotelName;
	/**
	 * @return the date
	 */
	public String getDate() {
		return date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(String date) {
		this.date = date;
	}
	/**
	 * @return the day
	 */
	public String getDay() {
		return day;
	}
	/**
	 * @param day the day to set
	 */
	public void setDay(String day) {
		this.day = day;
	}
	/**
	 * @return the hotelImage
	 */
	public String getHotelImage() {
		return hotelImage;
	}
	/**
	 * @param hotelImage the hotelImage to set
	 */
	public void setHotelImage(String hotelImage) {
		this.hotelImage = hotelImage;
	}
	/**
	 * @return the hotelInfo
	 */
	public String getHotelInfo() {
		return hotelInfo;
	}
	/**
	 * @param hotelInfo the hotelInfo to set
	 */
	public void setHotelInfo(String hotelInfo) {
		this.hotelInfo = hotelInfo;
	}
	/**
	 * @return the hotelName
	 */
	public String getHotelName() {
		return hotelName;
	}
	/**
	 * @param hotelName the hotelName to set
	 */
	public void setHotelName(String hotelName) {
		this.hotelName = hotelName;
	}
	/**
	 * @return the serialversionuid
	 */
	public static long getSerialversionuid() {
		return serialVersionUID;
	}

}
